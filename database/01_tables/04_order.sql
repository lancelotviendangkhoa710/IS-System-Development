    GhiChuThoBanh NVARCHAR2(255),
    DonGia NUMBER(18,2) NOT NULL,
    DonGiaVon NUMBER(18,2) NOT NULL,
    CONSTRAINT FK_CTDC_DON FOREIGN KEY (MaDon) REFERENCES DONDATHANG(MaDon),
    CONSTRAINT FK_CTDC_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP),
    CONSTRAINT FK_CTDC_SIZE FOREIGN KEY (MaSize) REFERENCES KICHCOBANH(MaSize),

CREATE TABLE TRANGTHAIDON (
    MaTrangThai NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    TenTrangThai NVARCHAR2(100) NOT NULL
);

CREATE TABLE DONDATHANG (
    MaDon NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NgayLap DATE DEFAULT SYSDATE,
    NgayGioNhanBanh DATE,
    MaKH NUMBER NOT NULL,
    MaNV_Lap NUMBER NOT NULL,
    MaTrangThai NUMBER NOT NULL,
    TongTienHDBan NUMBER(18,2) DEFAULT 0,
    TienDaCoc NUMBER(18,2) DEFAULT 0,
    CONSTRAINT FK_DON_KH FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH),
    CONSTRAINT FK_DON_NV FOREIGN KEY (MaNV_Lap) REFERENCES NHANVIEN(MaNV),
    CONSTRAINT FK_DON_TT FOREIGN KEY (MaTrangThai) REFERENCES TRANGTHAIDON(MaTrangThai)
);

CREATE TABLE CTDONHANG (
    MaCTDH NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    MaDon NUMBER NOT NULL,
    MaSP NUMBER NOT NULL,
    SoLuong NUMBER NOT NULL,
    DonGia NUMBER(18,2) NOT NULL,
    PhanTramGiam NUMBER(5,2) DEFAULT 0,
    DonGiaVon NUMBER(18,2) NOT NULL,
    CONSTRAINT FK_CTDH_DON FOREIGN KEY (MaDon) REFERENCES DONDATHANG(MaDon),
    CONSTRAINT FK_CTDH_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP)
);

CREATE TABLE CTDONCUSTOM (
